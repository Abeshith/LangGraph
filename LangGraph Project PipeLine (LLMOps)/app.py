import streamlit as st
import requests
import datetime
import sys

BASE_URL = "http://localhost:8000" 

st.set_page_config(
    page_title=" Travel Planner Agentic Application",
    page_icon="üåç",
    layout="centered",
    initial_sidebar_state="expanded",
)

st.title("üåç Travel Planner Agentic Application")

# Initialize chat history
if "messages" not in st.session_state:
    st.session_state.messages = []

# Display chat history
st.header("How can I help you in planning a trip? Let me know where do you want to visit.")

with st.form(key="query_form", clear_on_submit=True):
    user_input = st.text_input("User Input", placeholder="e.g. Plan a trip to Goa for 5 days")
    submit_button = st.form_submit_button("Send")

if submit_button and user_input.strip():
    try:
        # # Show user message
        # Show thinking spinner while backend processes
        with st.spinner("Bot is thinking..."):
            payload = {"question": user_input}
            response = requests.post(f"{BASE_URL}/query", json=payload, timeout=120)

        if response.status_code == 200:
            answer = response.json().get("answer", "No answer returned.")
            markdown_content = f"""# üåç AI Travel Plan

            # **Generated:** {datetime.datetime.now().strftime('%Y-%m-%d at %H:%M')}  
            # **Created by:** Atriyo's Travel Agent

            ---

            {answer}

            ---

            *This travel plan was generated by AI. Please verify all information, especially prices, operating hours, and travel requirements before your trip.*
            """
            st.markdown(markdown_content)
        else:
            st.error(f"Bot failed to respond. Status code: {response.status_code}")
            st.error(f"Response: {response.text}")

    except requests.exceptions.ConnectionError:
        st.error("üîå **Connection Error**: Unable to connect to the backend server.")
        st.info("Please make sure the backend server is running on http://localhost:8000")
        st.code("python main.py", language="bash")
    except requests.exceptions.Timeout:
        st.error("‚è±Ô∏è **Timeout Error**: The server took too long to respond.")
    except requests.exceptions.RequestException as e:
        st.error(f"üåê **Request Error**: {str(e)}")
    except Exception as e:
        st.error(f"‚ùå **Unexpected Error**: {str(e)}")
        st.info("Please try again or contact support if the issue persists.")